function buildStatusNote(agent, task) {
  return `進捗共有: ${task.name} を ${Math.round((task.progress || 0) * 100)}% まで進めています。`;
}

function randomRestMs() {
  return 2500 + Math.floor(Math.random() * 5500);
}

function createArtifact(task, agent) {
  const title = `${task.name} - ${agent.name}`;
  const stamp = new Date().toLocaleTimeString("ja-JP", { hour: "2-digit", minute: "2-digit" });

  switch (task.skill) {
    case "code":
    case "frontend":
    case "backend":
      return {
        kind: "code",
        title,
        body:
          `// generated by ${agent.name}\n` +
          `export function task_${task.id}() {\n` +
          `  return "${task.name} を実装した成果物";\n` +
          `}\n`,
        visualUri: null,
        stamp,
      };

    case "publish":
    case "deployment":
      return {
        kind: "page",
        title,
        body:
          `<section>\n` +
          `  <h1>${task.name}</h1>\n` +
          `  <p>${agent.name} が公開用の形に整えました。</p>\n` +
          `</section>`,
        visualUri: "https://picsum.photos/seed/publish-" + task.id + "/900/500",
        stamp,
      };

    case "video":
      return {
        kind: "video",
        title,
        body:
          `映像案:\n1. 導入\n2. 本題\n3. まとめ\n\n` +
          `${agent.name} が ${task.name} を動画構成に変換しました。`,
        visualUri: "https://picsum.photos/seed/video-" + task.id + "/900/500",
        stamp,
      };

    case "audio":
      return {
        kind: "audio",
        title,
        body:
          `音声案:\n- ナレーション\n- BGM\n- 効果音\n\n` +
          `${agent.name} が ${task.name} の音声設計を作成しました。`,
        visualUri: null,
        stamp,
      };

    case "design":
    case "ux":
    case "mobile":
      return {
        kind: "image",
        title,
        body:
          `UI案:\n- レイアウト整理\n- 視線誘導\n- 余白最適化\n\n` +
          `${agent.name} が ${task.name} をデザインしました。`,
        visualUri: "https://picsum.photos/seed/design-" + task.id + "/900/500",
        stamp,
      };

    case "research":
    case "analyze":
      return {
        kind: "report",
        title,
        body:
          `要点:\n- 主要な傾向を抽出\n- 競合や市場を比較\n- 次の打ち手を提案\n\n` +
          `${agent.name} が ${task.name} を分析しました。`,
        visualUri: null,
        stamp,
      };

    case "translate":
    case "localize":
      return {
        kind: "text",
        title,
        body:
          `原文に自然な多言語表現を付与しました。\n` +
          `${agent.name} が ${task.name} を翻訳しました。`,
        visualUri: null,
        stamp,
      };

    default:
      return {
        kind: "text",
        title,
        body:
          `${agent.name} が ${task.name} を完了しました。\n` +
          `実行時刻: ${stamp}`,
        visualUri: null,
        stamp,
      };
  }
}

function chooseRecipient(agents, sender, task) {
  const targetSkill = findCompanionSkill(task.skill);
  const candidates = agents.filter(
    (a) =>
      a.id !== sender.id &&
      a.status !== "rest" &&
      (a.skills.includes(targetSkill) || a.skills.includes("workflow") || a.skills.includes("review"))
  );

  if (candidates.length) return pickRandom(candidates);
  const fallback = agents.filter((a) => a.id !== sender.id);
  return fallback.length ? pickRandom(fallback) : null;
}

function pushLog(logs, kind, text) {
  logs.push({
    id: `${kind}-${Date.now()}-${Math.floor(Math.random() * 100000)}`,
    ts: Date.now(),
    kind,
    text,
  });
}

function trimArray(arr, max) {
  if (arr.length > max) return arr.slice(arr.length - max);
  return arr;
}

export function stepEngine(state) {
  const now = Date.now();
  const agents = state.agents.map(cloneAgent);
  const tasks = state.tasks.map(cloneTask);
  const logs = [...state.logs];

  const taskMap = new Map(tasks.map((t) => [t.id, t]));

  for (const agent of agents) {
    if (agent.restUntil && agent.restUntil <= now) {
      agent.restUntil = 0;
      if (agent.status === "rest") {
        agent.status = "idle";
        pushLog(logs, "system", `${agent.name} は休憩を終えました。`);
      }
    } else if (agent.restUntil && agent.restUntil > now) {
      agent.status = "rest";
    }

    while (agent.inbox.length) {
      const msg = agent.inbox.shift();
      const reply = buildReply(agent, msg);
      agent.outbox.push(reply);
      agent.history.push({ ts: now, type: "in", text: msg });
      agent.history.push({ ts: now, type: "out", text: reply });
      pushLog(logs, "chat", `${agent.name} 受信: ${msg}`);
      pushLog(logs, "chat", `${agent.name} 返信: ${reply}`);
    }

    agent.history = trimArray(agent.history, 25);
    agent.outbox = trimArray(agent.outbox, 20);
  }

  for (const agent of agents) {
    if (!agent.currentTaskId) continue;

    const task = taskMap.get(agent.currentTaskId);
    if (!task || task.status === "done") {
      agent.currentTaskId = null;
      agent.currentProgress = 0;
      agent.status = "idle";
      continue;
    }

    const speed =
      0.12 +
      (agent.skills.includes(task.skill) ? 0.18 : 0) +
      (task.skill === agent.primarySkill ? 0.16 : 0) +
      Math.random() * 0.10;

    task.progress = clamp((task.progress || 0) + speed, 0, 1);
    task.updatedAt = now;
    agent.currentProgress = task.progress;

    if (Math.random() < 0.18) {
      const recipient = chooseRecipient(agents, agent, task);
      if (recipient) {
        const note = buildStatusNote(agent, task);
        recipient.inbox.push(note);
        pushLog(logs, "chat", `${agent.name} → ${recipient.name}: ${note}`);
      }
    }

    if (task.progress >= 1) {
      task.progress = 1;
      task.status = "done";
      task.completedAt = now;
      task.completedBy = agent.id;
      task.completedByName = agent.name;
      task.output = createArtifact(task, agent);
      agent.currentTaskId = null;
      agent.currentProgress = 1;
      agent.status = "idle";
      agent.artifact = task.output;
      agent.history.push({ ts: now, type: "done", text: `${task.name} を完了` });
      pushLog(logs, "task", `完了: ${agent.name} が「${task.name}」を仕上げました。`);
    } else {
      task.status = "running";
      agent.status = "busy";
    }
  }

  const pending = tasks
    .filter((t) => t.status !== "done" && !t.assignedTo)
    .sort((a, b) => (b.priority || 0) - (a.priority || 0) || a.id - b.id);

  for (const agent of agents) {
    if (agent.status !== "idle" || agent.currentTaskId) continue;

    const task = pending.find((t) => agent.skills.includes(t.skill) && !t.assignedTo);
    if (task) {
      task.assignedTo = agent.id;
      task.assignedToName = agent.name;
      task.startedAt = now;
      task.status = "running";
      agent.currentTaskId = task.id;
      agent.currentProgress = task.progress || 0;
      agent.status = "busy";
      pushLog(logs, "task", `開始: ${agent.name} が「${task.name}」を担当しました。`);

      const recipient = chooseRecipient(agents, agent, task);
      if (recipient && Math.random() < 0.45) {
        const note = `着手しました: ${task.name}`;
        recipient.inbox.push(note);
        pushLog(logs, "chat", `${agent.name} → ${recipient.name}: ${note}`);
      }
    } else if (Math.random() < 0.05) {
      agent.status = "rest";
      agent.restUntil = now + randomRestMs();
      pushLog(logs, "system", `${agent.name} は自律的に休憩に入りました。`);
    }
  }

  if (Math.random() < 0.35) {
    const speaker = pickRandom(agents.filter((a) => a.status !== "rest"));
    const listener = speaker ? chooseRecipient(agents, speaker, { skill: "workflow" }) : null;
    if (speaker && listener) {
      const smallTalk = `進捗確認: ${speaker.currentTaskId ? "作業中" : "次の仕事を待機中"}。`;
      listener.inbox.push(smallTalk);
      pushLog(logs, "chat", `${speaker.name} → ${listener.name}: ${smallTalk}`);
    }
  }

  for (const agent of agents) {
    if (!agent.currentTaskId && !agent.restUntil) {
      agent.status = "idle";
    }
  }

  return {
    ...state,
    agents,
    tasks,
    logs: trimArray(logs, 160),
  };
             }
