function cloneAgent(agent) {
  return {
    ...agent,
    inbox: [...agent.inbox],
    outbox: [...agent.outbox],
    history: [...agent.history],
  };
}

function cloneTask(task) {
  return { ...task };
}

function clamp(n, min, max) {
  return Math.max(min, Math.min(max, n));
}

function pickRandom(list) {
  return list[Math.floor(Math.random() * list.length)];
}

